package de.frittenburger.coffee.model;

public class MetricLimit {
	
	private int max_per_hour;
	
	private int max_per_day;

	public int getMax_per_hour() {
		return max_per_hour;
	}

	public void setMax_per_hour(int max_per_hour) {
		this.max_per_hour = max_per_hour;
	}

	public int getMax_per_day() {
		return max_per_day;
	}

	public void setMax_per_day(int max_per_day) {
		this.max_per_day = max_per_day;
	}

}
